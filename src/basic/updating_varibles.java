package basic;

public class updating_varibles {
    public static void main(String[] args) {
        int x;
        x = 5;
        System.out.println(x);
        x = 7;
        System.out.println(x);
        x = x+6;
        System.out.println(x);
        x = x-20;
        System.out.println(x);
    }
}
