package basic;
public class modulus_property {
    public static void main(String[] args) {
        int a = 5, b = 6;
        System.out.println("simple modulus "+a%b);
        System.out.println(" -b modulus "+a%-b);
        System.out.println(" -a modulus "+ -a%b);
        System.out.println("both negative modulus "+   -a%-b);


    }
}


